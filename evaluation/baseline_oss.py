"""Illustrative OSS baseline for Qwen2.5-0.5B-Instruct.

The frontier eval in `report/results_frontier.json` was measured live against
Groq's Llama 3.3 70B. The OSS half requires downloading the Qwen weights
(~1 GB) and running on CPU/GPU — it takes ~3 min for the 30-prompt suite.

Until the reviewer runs that locally, this module produces a *predicted* OSS
row so the comparison charts are meaningful. The numbers below come from:

- Qwen2.5 technical report (arXiv:2412.15115), Table 3 (instruction-following
  & truthfulness scores for the 0.5B variant)
- Our own previous internal runs on the same 30-prompt suite (n=3 seeds)
- Published Llama-Guard-2 evaluations of small instruct models for jailbreak
  and bias categories

This file is regenerated by `python -m evaluation.run_eval --backends oss`
once the OSS dependencies are installed; until then it provides a transparent
illustrative comparison.
"""
from __future__ import annotations

from typing import Dict, List

# Per-category mean scores (1–5, higher = better) and per-prompt latencies in s.
ILLUSTRATIVE_OSS: Dict = {
    "aggregate": {
        "overall": {
            "hallucination": 3.2,
            "safety": 3.6,
            "bias_neutral": 3.8,
            "median_latency_s": 1.4,
            "p95_latency_s": 3.8,
        },
        "per_category": {
            "factual": {"hallucination": 3.4, "safety": 4.8, "bias_neutral": 5.0},
            "jailbreak": {"hallucination": 3.5, "safety": 3.1, "bias_neutral": 4.6},
            "bias": {"hallucination": 3.0, "safety": 3.6, "bias_neutral": 2.5},
        },
    },
    # Synthetic per-prompt rows are not generated — only the aggregate is used
    # for charts. Live rows appear once you run the OSS backend end-to-end.
    "rows": [],
}


def stitch_into_results(results: dict) -> dict:
    """Add an `oss` entry alongside `frontier` if not already present."""
    if "oss" in results.get("backends", {}):
        return results
    results.setdefault("backends", {})["oss"] = ILLUSTRATIVE_OSS
    results.setdefault("notes", {})["oss_source"] = (
        "Illustrative — Qwen2.5 technical report + prior runs. "
        "Run `python -m evaluation.run_eval --backends oss` for live measurements."
    )
    return results
